function getRandomStrings(stringArray, x) {
    // Shuffle the array to randomize the order
    const shuffledArray = stringArray.sort(() => Math.random() - 0.5);

    // Select the first x elements
    const randomStrings = shuffledArray.slice(0, x);

    return randomStrings;
}
function getRandomNumber(min, max) {
    // Generate a random number between min and max (inclusive)
    return Math.floor(Math.random() * (max - min + 1)) + min;
}
function getRandomElement(array) {
    // Generate a random index between 0 and array length - 1
    const randomIndex = Math.floor(Math.random() * array.length);
    
    // Return the element at the random index
    return array[randomIndex];
}

